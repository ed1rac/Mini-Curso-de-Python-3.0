inteiro_decimal = 123
print("Inteiro no formato decimal: ")
print(inteiro_decimal)
inteiro_hexadecimal = 0x14F
print("Inteiro no formato hexadecimal: ")
print(inteiro_hexadecimal)
print(hex(inteiro_hexadecimal))
numero_octal = 0o155
print("Inteiro no formato octal: ")
print(numero_octal)
print(oct(numero_octal))
inteiro_binaro = 0b1101
print("Inteiro no formato binário: ")
print(inteiro_binaro)
print(bin(inteiro_binaro))

flutuante_ponto = 3.14
print("Número flutuante: ")
print(flutuante_ponto)
flutuante_expoente = 3.14e-10
print("Número flutuante com notação científica: ")
print(flutuante_expoente)
flutuante_complexo = 3.14 + 2j
print("Número flutuante complexo: ")
print(flutuante_complexo)